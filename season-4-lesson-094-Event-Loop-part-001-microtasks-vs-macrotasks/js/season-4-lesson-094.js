/*Микротаски имеют приоритет выполнения пеед макротасками. Видим 1 -> 3 -> 2.*/
console.log(1); // Обычный код

setTimeout(() => { console.log(2) }, 0); // Макротаска

let promise01 = new Promise((resolve) => { resolve() }); // Обычный код

promise01.then(() => { console.log(3) }); // Микротаска