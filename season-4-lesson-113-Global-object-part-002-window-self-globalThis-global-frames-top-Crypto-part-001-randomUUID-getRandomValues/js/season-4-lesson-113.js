/*Объект "crypto" предоставляет базовые криптографические возможности, доступные в текущем контексте. Может 
использоваться через глобальный объект.*/
console.log(crypto === window.crypto); // true

/*Метод "crypto.randomUUID()" используется для генерации UUID версии 4 с помощью криптографически защищенного генератора 
случайных чисел.*/
console.log(crypto.randomUUID());

/*Метод "crypto.getRandomValues()" позволяет получать криптографически стойкие случайные значения. В качестве параметра
принимает типизированные целочисленный массив и возвращает ссылку на тот же массив, но уже заполненный случайными в 
криптографическом смысле числами.*/
const array_01 = new Uint32Array(10);
console.log(crypto.getRandomValues(array_01));