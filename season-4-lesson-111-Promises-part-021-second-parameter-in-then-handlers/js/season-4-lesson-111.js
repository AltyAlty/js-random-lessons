'use strict';

/*--------------------------------------------------------------------------------------------------------------------*/

/*Метод "then()" может принимать второй параметр в виде callback-функции, которая будет вызвана если промис будет 
rejected. То есть таким образом можно сделать аналог метода "catch()".*/
new Promise(((res, rej) => rej()))
    .then(
        () => console.log('Everything is fine'),
        () => console.log('Everything is bad')
    );